extern struct netif gnetif;
/* USER CODE END Header_StartTaskDhcpCheck */
void StartTaskDhcpCheck(void *argument)
{
  /* USER CODE BEGIN StartTaskDhcpCheck */
  /* Infinite loop */
  for(;;)
  {
    // Проверяем, поднят ли сетевой интерфейс и получен ли валидный IP
    if (netif_is_up(&gnetif) && gnetif.ip_addr.addr != 0)
    {
      uint8_t ip[4];
      ip[0] = ip4_addr1(&gnetif.ip_addr);
      ip[1] = ip4_addr2(&gnetif.ip_addr);
      ip[2] = ip4_addr3(&gnetif.ip_addr);
      ip[3] = ip4_addr4(&gnetif.ip_addr);

      printf("DHCP IP: %d.%d.%d.%d\r\n", ip[0], ip[1], ip[2], ip[3]);

      osStatus_t status;
      status = osThreadTerminate(dhcp_taskHandle);
      if (status == osOK) {
        // Задача успешно удалена.
        // ВАЖНО: обнулите хендл, чтобы не попытаться обратиться к нему снова.
        dhcp_taskHandle = NULL;
      }
    }
    else
    {
      osDelay(500); // Ждем получения IP
    }
  }
  /* USER CODE END StartTaskDhcpCheck */
}

